def safe_str(text, maxlen=None):
    if not text: return ""
    text = str(text)
    if maxlen: text = text[:maxlen]
    try:
        text.encode("latin-1")
        return text
    except (UnicodeEncodeError, UnicodeDecodeError):
        return text.encode("ascii", errors="replace").decode("ascii")

