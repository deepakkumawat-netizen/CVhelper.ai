def run_pptx(data_dict):
    tmp_json = tmp_pptx = None
    try:
        with tempfile.NamedTemporaryFile(mode="w", suffix=".json",
                                         delete=False, encoding="utf-8") as f:
            json.dump(data_dict, f, ensure_ascii=False)
            tmp_json = f.name
        tmp_pptx = tmp_json.replace(".json", ".pptx")
        res = subprocess.run(
            ["node", PPTX_JS, tmp_json, tmp_pptx],
            capture_output=True, text=True, timeout=60
        )
        if res.returncode != 0:
            raise RuntimeError(res.stderr or res.stdout or "node error")
        if not os.path.exists(tmp_pptx):
            raise RuntimeError("PPTX not created")
        with open(tmp_pptx, "rb") as f:
            return base64.b64encode(f.read()).decode()
    finally:
        for p in [tmp_json, tmp_pptx]:
            if p:
                try: os.unlink(p)
                except Exception: pass

