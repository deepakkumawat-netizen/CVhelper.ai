def generate_topic_content(topic, course_title, class_title, level, lang, position, total, level_instruction=None):
    """Generate rich, unique content for ONE topic only — always in English."""
    print(f"  [topic {position}/{total}] Researching: {topic}")
    knowledge = fetch_real_knowledge(topic, course_title)
    print(f"  [topic {position}/{total}] Got {len(knowledge)} chars of web content")

    persona_note = f"TEACHING PERSONA: {level_instruction}" if level_instruction else ""

    prompt = (
        f"You are an expert educator. Write educational content about ONE specific topic.\n"
        f"{persona_note}\n\n"
        f"Topic: {topic}\n"
        f"Course: {course_title}\n"
        f"Class: {class_title}\n"
        f"Level: {level}\n"
        f"Position in class: {position} of {total} topics\n\n"
        f"REFERENCE MATERIAL FROM THE WEB:\n{knowledge}\n\n"
        f"Write a detailed educational section about '{topic}' using the reference material.\n"
        f"IMPORTANT: This is topic #{position} in the class. Do NOT repeat content from other topics.\n"
        f"Focus ONLY on '{topic}' — make the content specific, factual, and unique to this topic.\n"
        f"Do NOT use generic placeholder phrases. Write real, specific, factual content.\n\n"
        f"Return ONLY this JSON — no markdown, no extra text:\n"
        f'{{\n'
        f'  "heading": "{topic}",\n'
        f'  "body": "3-4 sentences explaining {topic} with a clear definition, a real-world example, and why it matters in {course_title}",\n'
        f'  "bullets": [\n'
        f'    "A specific, verified fact about {topic}",\n'
        f'    "How professionals use {topic} in real work",\n'
        f'    "The most common beginner mistake with {topic} and how to avoid it",\n'
        f'    "One expert tip for mastering {topic}"\n'
        f'  ]\n'
        f'}}'
    )

    try:
        raw  = ai([{"role": "user", "content": prompt}], max_tokens=1000, temp=0.7)
        data = extract_json(raw)

        body    = data.get("body", "")
        bullets = data.get("bullets", [])
        heading = data.get("heading", topic)

        bad_phrases = ["write ", "Write ", "3-4 sentences", "complete sentence",
                       "specific verified fact", "concrete real-world", "topic name"]
        if any(p in body for p in bad_phrases) or len(body) < 60:
            raise ValueError(f"Placeholder text detected for: {topic}")

        return {"heading": heading, "body": body, "bullets": bullets[:4]}

    except Exception as e:
        print(f"  [topic {position}/{total}] AI failed: {e}, using fallback")
        lines = [l.strip() for l in knowledge.split('\n') if len(l.strip()) > 40]
        body  = " ".join(lines[:3])[:400] if lines else (
            f"{topic} is a fundamental concept in {course_title}. "
            f"Mastering {topic} enables you to work confidently and professionally."
        )
        return {
            "heading": topic,
            "body":    body,
            "bullets": [
                f"{topic} is a core building block in {course_title}",
                f"Professionals apply {topic} to improve quality and efficiency",
                f"Beginners often overlook {topic} — focus on understanding the why",
                f"Daily practice of {topic} leads to faster mastery"
            ]
        }

