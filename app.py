def call_gemini(messages, max_tokens=2000, temp=0.7):
    key = os.environ.get("GEMINI_API_KEY", "").strip()
    if not key:
        raise ValueError("GEMINI_API_KEY not set")
    contents, system_text = [], ""
    for m in messages:
        role, content = m.get("role", "user"), m.get("content", "")
        if role == "system":
            system_text = content
        elif role == "user":
            contents.append({"role": "user",  "parts": [{"text": content}]})
        elif role == "assistant":
            contents.append({"role": "model", "parts": [{"text": content}]})
    if system_text and contents:
        contents[0]["parts"][0]["text"] = system_text + "\n\n" + contents[0]["parts"][0]["text"]
    elif system_text:
        contents = [{"role": "user", "parts": [{"text": system_text}]}]
    if not contents:
        raise ValueError("No messages")
    r = http.post(
        f"{GEMINI_URL}?key={key}",
        headers={"Content-Type": "application/json"},
        json={"contents": contents,
              "generationConfig": {"temperature": temp, "maxOutputTokens": max_tokens}},
        timeout=120
    )
    r.raise_for_status()
    data       = r.json()
    candidates = data.get("candidates", [])
    if not candidates:
        raise ValueError(f"Gemini: no candidates. {data}")
    parts = candidates[0].get("content", {}).get("parts", [])
    if not parts:
        raise ValueError(f"Gemini: empty parts. finishReason={candidates[0].get('finishReason')}")
    return parts[0]["text"].strip()

def call_groq(messages, max_tokens=2000, temp=0.7):
    key = os.environ.get("GROQ_API_KEY", "").strip()
    if not key:
        raise ValueError("GROQ_API_KEY not set")
    r = http.post(GROQ_URL,
        headers={"Authorization": f"Bearer {key}", "Content-Type": "application/json"},
        json={"model": GROQ_MODEL, "messages": messages,
              "max_tokens": max_tokens, "temperature": temp},
        timeout=90)
    r.raise_for_status()
    return r.json()["choices"][0]["message"]["content"].strip()

def ai(messages, max_tokens=2000, temp=0.7):
    engine    = os.environ.get("AI_ENGINE", "auto").lower()
    gem_key   = os.environ.get("GEMINI_API_KEY", "").strip()
    groq_key  = os.environ.get("GROQ_API_KEY",   "").strip()
    if engine == "gemini": return call_gemini(messages, max_tokens, temp)
    if engine == "groq":   return call_groq(messages, max_tokens, temp)
    last_err = None
    if gem_key:
        try:   return call_gemini(messages, max_tokens, temp)
        except Exception as e:
            last_err = e
            if groq_key: print(f"[CVhelper] Gemini failed ({e}), trying Groq…")
    if groq_key:
        try:   return call_groq(messages, max_tokens, temp)
        except Exception as e: last_err = e
    raise ValueError(f"All AI engines failed. Last: {last_err}")

def groq(messages, max_tokens=2000, temp=0.7):
    return ai(messages, max_tokens, temp)

