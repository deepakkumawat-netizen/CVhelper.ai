def search_wikipedia(query):
    """Search Wikipedia for a topic and return its summary."""
    try:
        search_url = "https://en.wikipedia.org/w/api.php"
        sr = http.get(search_url, params={
            "action": "query", "list": "search",
            "srsearch": query, "srlimit": 1, "format": "json"
        }, timeout=8, headers={"User-Agent": "CVhelper.ai/1.0"})
        sr.raise_for_status()
        results = sr.json().get("query", {}).get("search", [])
        if not results:
            return ""
        title = results[0]["title"]
        pr = http.get(search_url, params={
            "action": "query", "prop": "extracts",
            "exintro": True, "explaintext": True,
            "titles": title, "format": "json"
        }, timeout=8, headers={"User-Agent": "CVhelper.ai/1.0"})
        pr.raise_for_status()
        pages = pr.json().get("query", {}).get("pages", {})
        for page in pages.values():
            extract = page.get("extract", "")
            if extract and len(extract) > 100:
                return f"[Wikipedia: {title}]\n{extract[:1000]}"
    except Exception as e:
        print(f"[Wikipedia search] {e}")
    return ""

def search_duckduckgo(query):
    """DuckDuckGo instant answer — free, no key needed."""
    try:
        r = http.get("https://api.duckduckgo.com/", params={
            "q": query, "format": "json",
            "no_redirect": 1, "no_html": 1, "skip_disambig": 1
        }, timeout=8, headers={"User-Agent": "CVhelper.ai/1.0"})
        r.raise_for_status()
        data = r.json()
        text = ""
        if data.get("AbstractText"):
            text += f"[{data.get('Heading',query)}]: {data['AbstractText'][:600]}\n"
        for t in data.get("RelatedTopics", [])[:3]:
            if isinstance(t, dict) and t.get("Text") and len(t["Text"]) > 40:
                text += f"- {t['Text'][:300]}\n"
        return text.strip()
    except Exception as e:
        print(f"[DDG search] {e}")
    return ""

def fetch_real_knowledge(topic, course_title):
    """Fetch real educational content about a topic from the web."""
    queries = [
        f"{topic} {course_title}",
        f"what is {topic}",
        f"{topic} explained"
    ]
    knowledge = ""
    for q in queries[:2]:
        wiki = search_wikipedia(q)
        if wiki and len(wiki) > 100:
            knowledge += wiki + "\n\n"
            break
    ddg = search_duckduckgo(f"{topic} {course_title}")
    if ddg and len(ddg) > 50:
        knowledge += ddg
    return knowledge.strip() or f"Topic: {topic} in the context of {course_title}."

