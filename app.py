def extract_json(text):
    if not text: raise ValueError("Empty AI response")
    text = text.strip()
    m = re.search(r"```(?:json)?\s*([\s\S]+?)```", text)
    if m: text = m.group(1).strip()

    def try_parse(s):
        try: return json.loads(s)
        except json.JSONDecodeError:
            s2 = re.sub(r',\s*([\}\]])', r'\1', s)
            s2 = re.sub(r'[\x00-\x08\x0b\x0c\x0e-\x1f\x7f]', '', s2)
            return json.loads(s2)

    depth = start = -1
    for i, ch in enumerate(text):
        if ch == '{':
            if start == -1: start = i
            depth = (depth or 0) + 1
        elif ch == '}' and start != -1:
            depth -= 1
            if depth == 0:
                try: return try_parse(text[start:i+1])
                except Exception: start = depth = -1

    s, e = text.find("{"), text.rfind("}")
    if s != -1 and e > s: return try_parse(text[s:e+1])
    raise ValueError(f"No JSON in: {text[:200]}")

